INCLUDE 'EMU8086.INC'

.MODEL SMALL
.STACK 100H
.DATA
    RESULT DB ?
    STORE DB ?
.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    PRINT "TAKE INPUT NUMBER FROM 0 TO 9 : "
    
    MOV AL,1
    INT 21H
    
    MOV STORE,AL  ;STORE INPUT VALUE 
    ;CHARATER TO DECIMAL
    SUB AL,'0'
    AND AL,1
    MOV RESULT,AL 
    
    ;PRINT INPUT NUMBER
    MOV DL,STORE
    MOV AH,2
    INT 21H
    
    ;NEWLINE
    MOV AH,2
    MOV DL,10
    INT 21H
    MOV DL,13
    INT 21H
    
    
    ;OUTPUT
    CMP RESULT,0
    JE EVEN
    PRINT "ODD NUMBER"
EVEN:
    CMP RESULT,0
    JNE END
    PRINT "EVEN NUMBER"

END:
    MOV AX,4CH
    INT 21H
MAIN ENDP
END MAIN